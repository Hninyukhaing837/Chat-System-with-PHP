  2.  We will have a Game Session that will make them feel comfortable with club and new comers for we have seen that most of the new comers are afraid in their very first time.<br>
  3.  We will give a presentation to them by one of our volunteers about something that was announced on our Facebook Page. They could have learnt how to give a presentation very well. And after that, they will have a small activity like writing their dreams down on paper or have a discussion with groups then, one of the representative will share what they had discussed in their group.<br>
