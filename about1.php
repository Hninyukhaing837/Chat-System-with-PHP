<p2 id="para">ELC is for everyone who's currently learning English and has an interest on English Language because we share knowledge and facts about any subject, people, country and so many things using English.<br>
 So we don't need to have any level in English and the fact is, we are not going to teach but train members to be brave to speak out in public, to share knowledge to each other and to make a more convenient atmosphere with mates who are eager to learn English. Besides, the main problem of foreign language learning students is that they don't dare to speak out but we want to do and they don't know how to do. That's why we are here and we believe that people who join our club will be the best speaker in English and killer of all presentation.
